/** \brief Suma los dos operandos
 *
 * \param int Primer operando
 * \param int Segundo operando
 * \return int Regresa la suma
 *
 */
int sumar(int, int);

/** \brief Resta los dos operandos
 *
 * \param int Primer operando
 * \param int Segundo operando
 * \return int regresa la resta
 *
 */
int resta(int, int);

/** \brief Multiplica los openados
 *
 * \param int primer operando
 * \param int segundo operando
 * \return int retorna la multiplicacion o 0 si se intenta multiplicar algun numero por 0
 *
 */
int multiplicacion(int, int);

/** \brief Divide los operandos
 *
 * \param int primer operando
 * \param int segundo operando
 * \return float regresa la division de los operandos, o 0 si alguno era 0
 *
 */
float division(int, int);

/** \brief Saca el factorial de un numero, si es negativo presenta un mensaje de error
 *
 * \param int el operando que recibe
 * \return int el valor del factorial o 0 si no puede tener su factorial
 *
 */
int factorial(int);
